function formatKeywordsForAI(kwObj) {
  if (!kwObj || typeof kwObj !== 'object') return '';
  var lines = [];
  Object.keys(kwObj).forEach(function(key) {
    var val = kwObj[key];
    if (Array.isArray(val)) {
      lines.push('- ' + key + ': ' + val.join(', '));
    } else if (typeof val === 'string') {
      lines.push('- ' + key + ': ' + val);
    }
  });
  return lines.join('\n');
}

