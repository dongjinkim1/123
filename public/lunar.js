      KoreanLunarCalendar2.prototype.getLunarData = function(year) {
        return LUNAR_CALENDAR_DATA.KOREAN_LUNAR_DATA[year - LUNAR_CALENDAR_DATA.KOREAN_LUNAR_BASE_YEAR];
      };
      KoreanLunarCalendar2.prototype.getLunarIntercalationMonth = function(lunarData) {
        return lunarData >> 12 & 15;
      };
      KoreanLunarCalendar2.prototype.getLunarDays = function(year) {
        var lunarData = this.getLunarData(year);
        return lunarData >> 17 & 511;
      };
      KoreanLunarCalendar2.prototype.getLunarDays2 = function(year, month, isIntercalation) {
        var days = 0;
        var lunarData = this.getLunarData(year);
        if (isIntercalation && this.getLunarIntercalationMonth(lunarData) == month) {
          days = (lunarData >> 16 & 1) > 0 ? LUNAR_CALENDAR_DATA.LUNAR_BIG_MONTH_DAY : LUNAR_CALENDAR_DATA.LUNAR_SMALL_MONTH_DAY;
        } else {
          days = (lunarData >> 12 - month & 1) > 0 ? LUNAR_CALENDAR_DATA.LUNAR_BIG_MONTH_DAY : LUNAR_CALENDAR_DATA.LUNAR_SMALL_MONTH_DAY;
        }
        return days;
      };
      KoreanLunarCalendar2.prototype.getLunarDaysBeforeBaseYear = function(year) {
        var days = 0;
        for (var baseYear = LUNAR_CALENDAR_DATA.KOREAN_LUNAR_BASE_YEAR; baseYear < year + 1; baseYear++) {
          days += this.getLunarDays(baseYear);
        }
        return days;
      };
      KoreanLunarCalendar2.prototype.getLunarDaysBeforeBaseMonth = function(year, month, isIntercalation) {
        var days = 0;
        if (year >= LUNAR_CALENDAR_DATA.KOREAN_LUNAR_BASE_YEAR && month > 0) {
          for (var baseMonth = 1; baseMonth < month + 1; baseMonth++) {
            days += this.getLunarDays2(year, baseMonth, false);
          }
          if (isIntercalation) {
            var intercalationMonth = this.getLunarIntercalationMonth(this.getLunarData(year));
            if (intercalationMonth > 0 && intercalationMonth < month + 1) {
              days += this.getLunarDays2(year, intercalationMonth, true);
            }
          }
        }
        return days;
      };
      KoreanLunarCalendar2.prototype.getLunarAbsDays = function(year, month, day, isIntercalation) {
        var days = 0;
        days = this.getLunarDaysBeforeBaseYear(year - 1) + this.getLunarDaysBeforeBaseMonth(year, month - 1, true) + day;
        if (isIntercalation && this.getLunarIntercalationMonth(this.getLunarData(year)) == month) {
          days += this.getLunarDays2(year, month, false);
        }
        return days;
      };
