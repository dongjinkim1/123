          this.gapjaMonthInx[1] = (monthCount + 1) % LUNAR_CALENDAR_DATA.KOREAN_GANJI.length;
          this.gapjaDayInx[0] = (absDays + 4) % LUNAR_CALENDAR_DATA.KOREAN_CHEONGAN.length;
